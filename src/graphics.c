void pixel(int x, int y, u8 c) { if(x>=0 && y>=0 && x<320 && y<200) back_buffer[y*320+x] = c; }
void rect(int x, int y, int w, int h, u8 c) { for(int yy=y; yy<y+h; yy++) for(int xx=x; xx<x+w; xx++) pixel(xx, yy, c); }

void draw_char(int x, int y, char c, u8 color) {
    int idx = 36; if(c>='a'&&c<='z') c-=32;
    if(c>='A'&&c<='Z') idx=c-'A'; else if(c>='0'&&c<='9') idx=26+(c-'0');
    else if(c=='*') idx=37; else if(c=='^') idx=38; else if(c=='-') idx=39;
    else if(c=='>') idx=40; else if(c=='+') idx=41; else if(c=='/') idx=42;
    else if(c=='(') idx=43; else if(c==')') idx=44; else if(c=='=') idx=45;
    for(int r=0; r<8; r++) { u8 b=font[idx][r]; for(int col=0; col<8; col++) if(b&(1<<(7-col))) pixel(x+col, y+r, color); }
}

void draw_text(int x, int y, const char* s, u8 c) { for(int i=0; s[i]; i++) draw_char(x+(i*8), y, s[i], c); }
void draw_icon(int x, int y, u8* data) { for(int i=0; i<16; i++) for(int j=0; j<16; j++) if(data[i*16+j] != 0) pixel(x+j, y+i, data[i*16+j]); }
void flip() { u32* d=(u32*)VGA; u32* s=(u32*)back_buffer; for(int i=0; i<16000; i++) d[i]=s[i]; }

void draw_3d_button(int x, int y, int w, int h, u8 bg_color) {
    rect(x, y, w, h, bg_color); 
    for(int i = 0; i < w; i++) pixel(x + i, y, 15);
    for(int i = 0; i < h; i++) pixel(x, y + i, 15);
    for(int i = 0; i < w; i++) pixel(x + i, y + h - 1, 0);
    for(int i = 0; i < h; i++) pixel(x + w - 1, y + i, 0);
    for(int i = 1; i < w - 1; i++) pixel(x + i, y + h - 2, 8);
    for(int i = 1; i < h - 1; i++) pixel(x + w - 2, y + i, 8);
}

void draw_ui_window(struct Window* w, const char* title) {
    if(w->is_closed || w->is_minimized) return;
    rect(w->x, w->y, w->w, w->h, 7);      
    for(int i = 0; i < w->w; i++) pixel(w->x + i, w->y, 15);
    for(int i = 0; i < w->h; i++) pixel(w->x, w->y + i, 15);
    for(int i = 0; i < w->w; i++) pixel(w->x + i, w->y + w->h - 1, 0);
    for(int i = 0; i < w->h; i++) pixel(w->x + w->w - 1, w->y + i, 0);
    for(int i = 1; i < w->w - 1; i++) pixel(w->x + i, w->y + w->h - 2, 8);
    for(int i = 1; i < w->h - 1; i++) pixel(w->x + w->w - 2, w->y + i, 8);
    
    if (w->is_not_static){
        rect(w->x + 3, w->y + 3, w->w - 6, 14, 1); 
        draw_text(w->x + 5, w->y + 6, title, 15);
        
        if (w->has_minimize) {
            draw_3d_button(w->x + w->w - 29, w->y + 5, 12, 10, 7);
            draw_char(w->x + w->w - 27, w->y + 6, '-', 0);
        }
        
        draw_3d_button(w->x + w->w - 16, w->y + 5, 12, 10, 4);
        draw_char(w->x + w->w - 14, w->y + 6, 'X', 15);
    }
}

void draw_window_contents(struct Window* w) {
    if(w->is_closed || w->is_minimized) return;

    if (active_game == 0) {
        draw_icon(w->x + 28, w->y + 28, snake_icon);
        draw_text(w->x + 20, w->y + 48, "SNAKE", 0); 
        draw_icon(w->x + 98, w->y + 28, ttt_icon);
        draw_text(w->x + 86, w->y + 48, "TIC-TAC", 0); 
    } 
    else if (active_game == 1) {
        rect(w->x + 4, w->y + 20, 152, 56, 0); 
        if (snake_state == 0) {
            rect(w->x + 4 + (food_x * 4), w->y + 20 + (food_y * 4), 4, 4, 12);
            for (int i = 0; i < snake_len; i++) {
                rect(w->x + 4 + (snake_x[i] * 4), w->y + 20 + (snake_y[i] * 4), 4, 4, 10);
            }
        } else {
            draw_text(w->x + 44, w->y + 40, "GAME OVER", 12);
            draw_text(w->x + 50, w->y + 55, "CLICK", 15);
        }
    } 
    else if (active_game == 2) {
        int bx = w->x + 53;
        int by = w->y + 24;
        
        rect(bx + 17, by, 2, 54, 0);
        rect(bx + 35, by, 2, 54, 0);
        rect(bx, by + 17, 54, 2, 0);
        rect(bx, by + 35, 54, 2, 0);

        for (int y = 0; y < 3; y++) {
            for (int x = 0; x < 3; x++) {
                int p = ttt_board[y * 3 + x];
                if (p == 1) draw_text(bx + 5 + (x * 18), by + 5 + (y * 18), "X", 12); 
                if (p == 2) draw_text(bx + 5 + (x * 18), by + 5 + (y * 18), "O", 9);  
            }
        }

        if (ttt_winner != 0) {
            rect(w->x + 40, w->y + 40, 80, 20, 15); 
            if(ttt_winner == 1) draw_text(w->x + 52, w->y + 46, "X WINS!", 12);
            if(ttt_winner == 2) draw_text(w->x + 52, w->y + 46, "O WINS!", 9);
        }
    }
}